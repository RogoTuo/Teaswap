// require rimraf
const fs = require("fs");
const { execSync } = require("child_process");

execSync("rimraf node_modules");
for (const dir of fs.readdirSync("./packages")) {
  execSync(`rimraf ./packages/${dir}/node_modules`);
}
for (const dir of fs.readdirSync("./examples")) {
  execSync(`rimraf ./examples/${dir}/node_modules`);
}
